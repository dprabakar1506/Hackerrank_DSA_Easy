class Result {

    /*
     * Complete the 'strangeCounter' function below.
     *
     * The function is expected to return a LONG_INTEGER.
     * The function accepts LONG_INTEGER t as parameter.
     */

    public static long strangeCounter(long t) {
    // Write your code here
    long cycleStart = 1;   // Start time of current cycle
    long cycleValue = 3;   // Initial cycle value

    // Find the cycle in which time t lies
    while (t >= cycleStart + cycleValue) {
        cycleStart += cycleValue;
        cycleValue *= 2;   // Each cycle doubles in size
    }

    // cycleValue decreases one by one to 1
    return cycleValue - (t - cycleStart);

    }

}

public class Solution {
    public static void main(String[] args) throws IOException {
        BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(System.getenv("OUTPUT_PATH")));

        long t = Long.parseLong(bufferedReader.readLine().trim());

        long result = Result.strangeCounter(t);

        bufferedWriter.write(String.valueOf(result));
        bufferedWriter.newLine();

        bufferedReader.close();
        bufferedWriter.close();
    }
}
